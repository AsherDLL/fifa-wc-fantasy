"""Emit LaTeX table fragments and number macros for the paper.

Reads the same evaluation artifacts the dashboard uses (via
fifa_fantasy.report.data loaders) and writes booktabs fragments plus a
numbers.tex macro file into paper/generated/. The paper \\input{}s these
fragments so no computed number is ever hand-copied into prose.

    python scripts/paper_tables.py --out paper/generated
"""
from __future__ import annotations

import argparse
import sys
from pathlib import Path

ROOT = Path(__file__).resolve().parents[1]
sys.path.insert(0, str(ROOT / "src"))

from fifa_fantasy.report.data import (  # noqa: E402
    STAGE_LABEL,
    load_backtest,
    load_gk_sweep,
    load_market_negative,
    load_validation_report,
    load_walkforward,
)

HEADER = "% GENERATED by scripts/paper_tables.py -- do not edit\n"

# TeX control words are letters-only; spell out any digit.
_DIGITS = str.maketrans({"0": "Zero", "1": "One", "2": "Two", "3": "Three",
                         "4": "Four", "5": "Five", "6": "Six",
                         "7": "Seven", "8": "Eight", "9": "Nine"})


def _macro_name(name: str) -> str:
    return name.translate(_DIGITS)
BACKEND_LABEL = {
    "heuristic": "Heuristic",
    "heuristic_v2": "Heuristic v2",
    "poisson": "Poisson",
    "gbm": "GBM",
    "monte_carlo": "Monte Carlo",
    "ensemble": "Ensemble",
    "user_actual": "User (actual)",
    "random_baseline": "Random baseline",
}
CONFIG_LABEL = {
    "A_epl_noform": "A: EPL, no form",
    "B_epl_form": "B: EPL + form",
    "C_eplwc_form": "C: EPL+WC + form",
    "D_eplwc_all": "D: EPL+WC, proxy minutes",
    "E_eplwc_xg": "E: EPL+WC, real xG form",
    "F_eplwc_minutes": "F: EPL+WC, real minutes",
}

