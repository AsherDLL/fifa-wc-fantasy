def build_overview(results_dir: Path) -> str:
    now = datetime.now(timezone.utc)
    ctx = _common_context(results_dir, now, "overview")
    dirs = _root_dirs(results_dir)

    official, fallback = official_recommendation(results_dir)
    featured_extra = _augment_featured(official) if official else None

    per_backend = latest_recommendation_per_backend(results_dir)
    agreement = {"agree": "-", "n_backends": len(per_backend)}
    if official:
        own = set(official.get("squad_player_ids", []))
        others = [r for r in per_backend.values()
                  if r.get("__filename__") != official.get("__filename__")]
        if own and others:
            overlaps = [len(own & set(r.get("squad_player_ids", [])))
                        for r in others]
            agreement = {"agree": round(sum(overlaps) / len(overlaps)),
                         "n_backends": len(others) + 1}

    squad_signals: list[dict] = []
    squad_signals_total = 0
    if official:
        signals = load_signals(dirs["signals"], days=SIGNAL_WINDOW_DAYS)
        if not signals.empty:
            agg = aggregate_signals(signals)
            squad = signals_for_squad(agg,
                                      official.get("squad_player_ids", []))
            squad_signals_total = int(len(squad))
            # The overview is a decision page; the top of the triage order
            # belongs here and the full list lives on the intelligence page.
            squad_signals = _signal_rows(squad.head(20), now)

    history = []
    for group in _group_by_stage(list_recommendations(results_dir)):
        items = [r for r in group["items"]
                 if not official
                 or r.get("__filename__") != official.get("__filename__")]
        if items:
            history.append({**group, "items": items})
    history.reverse()

    bundle = _load_report_bundle(results_dir)
    return _env().get_template("overview.html.jinja").render(
        **ctx,
        official=official,
        official_is_fallback=fallback,
        official_backend=official_backend(),
        f=featured_extra,
        agreement=agreement,
        squad_signals=squad_signals,
        squad_signals_total=squad_signals_total,
        signal_days=int(SIGNAL_WINDOW_DAYS),
        history=history,
        final_plan=bundle.get("final_plan"),
        match_forecast=bundle.get("match_forecast"),
        figs=_figs(results_dir, ["fig_final_probs"]),
    )


def build_algorithms(results_dir: Path) -> str:
    now = datetime.now(timezone.utc)
    ctx = _common_context(results_dir, now, "algorithms")
    bundle = _load_report_bundle(results_dir)

    per_backend = latest_recommendation_per_backend(results_dir)
    backtest = bundle.get("backtest")

    tabs = []
    for record in MODEL_REGISTRY:
        rec = per_backend.get(record.key)
        row = None
        total = None
        if backtest and record.key in backtest.get("series", {}):
            row = backtest["series"][record.key]
            total = backtest["cumulative"][record.key][-1]
        tabs.append({
            "record": record,
            "rec": rec,
            "f": _augment_featured(rec) if rec else None,
            "formula_svg": _inline_svg(results_dir, f"formula_{record.key}"),
            "backtest_row": row,
            "backtest_total": total,
        })

    return _env().get_template("algorithms.html.jinja").render(
        **ctx,
        tabs=tabs,
        validation=bundle.get("validation") or [],
        routing=bundle.get("routing") or {},
        backtest=backtest,
    )


def build_intelligence(results_dir: Path) -> str:
    now = datetime.now(timezone.utc)
    ctx = _common_context(results_dir, now, "intelligence")
    dirs = _root_dirs(results_dir)
    bundle = _load_report_bundle(results_dir)

    signals = load_signals(dirs["signals"], days=SIGNAL_WINDOW_DAYS)
    agg = aggregate_signals(signals) if not signals.empty else pd.DataFrame()

    official, _ = official_recommendation(results_dir)
    squad_signals: list[dict] = []
    if official and not agg.empty:
        squad_signals = _signal_rows(
            signals_for_squad(agg, official.get("squad_player_ids", [])), now)

    coverage = bundle.get("signals_coverage") or {
        "articles_cached": 0, "signal_rows": int(len(signals)),
        "players_flagged": 0, "by_class": {}, "by_signal": {}}
    # Live corpus numbers where cheap; the bundle fills the rest.
    coverage = dict(coverage)
    coverage["signal_rows"] = int(len(signals))
    if not signals.empty:
        coverage["players_flagged"] = int(signals["player_id"].nunique())

    players_indexed = "-"
    pred_files = sorted(dirs["processed"].glob("predictions_*.parquet"))
    if pred_files:
        try:
            import pyarrow.parquet as pq
            col = pq.read_table(pred_files[-1], columns=["player_id"])
            players_indexed = f"{col['player_id'].to_pandas().nunique():d}"
        except Exception:
            players_indexed = "-"

    total_findings = int(len(agg)) if not agg.empty else 0
    if total_findings > SIGNAL_TABLE_CAP:
        agg = agg.head(SIGNAL_TABLE_CAP)

    return _env().get_template("intelligence.html.jinja").render(
        **ctx,
        signals=_signal_rows(agg, now),
        total_findings=total_findings,
        table_cap=SIGNAL_TABLE_CAP,
        squad_signals=squad_signals,
        coverage=coverage,
        players_indexed=players_indexed,
        signal_days=int(SIGNAL_WINDOW_DAYS),
        market_latest=bundle.get("market_latest") or [],
        figs=_figs(results_dir, ["fig_signal_volume", "fig_market_odds"]),
    )


def build_research(results_dir: Path) -> str:
    now = datetime.now(timezone.utc)
    ctx = _common_context(results_dir, now, "research")
    bundle = _load_report_bundle(results_dir)

    return _env().get_template("research.html.jinja").render(
        **ctx,
        timeline=TIMELINE,
        generations=GENERATIONS,
        negative_results=NEGATIVE_RESULTS,
        backtest=bundle.get("backtest"),
        backend_labels=BACKEND_LABELS,
        walkforward=bundle.get("walkforward"),
        calibration_summary=bundle.get("calibration_summary") or [],
        match_forecast=bundle.get("match_forecast"),
        figs=_figs(results_dir, [
            "fig_backtest_cumulative", "fig_backtest_rounds",
            "fig_holdout_rmse", "fig_walkforward", "fig_calibration",
            "fig_gk_sweep", "fig_market_negative", "fig_forecast_skill",
        ]),
    )


PAGES = {
    "index.html": build_overview,
    "algorithms.html": build_algorithms,
    "intelligence.html": build_intelligence,
    "research.html": build_research,
}


def resolve_page(path: str) -> str | None:
    """Map a request path to a page filename, or None for static paths."""
    name = path.split("?", 1)[0].split("#", 1)[0].lstrip("/")
    if not name:
        name = "index.html"
    if not name.endswith(".html"):
        name = f"{name}.html"
    return name if name in PAGES else None


def build_all(results_dir: Path) -> dict[str, str]:
    """Render every page; used by the static writer."""
    return {name: builder(results_dir) for name, builder in PAGES.items()}
